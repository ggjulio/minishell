#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	i;
	char	*res;

	if (!(res = malloc(sizeof(char) * ft_strlen(s) + 1)))
		return (NULL);
	i = -1;
	while (s[++i])
		res[i] = s[i];
	res[i] = '\0';
	return (res);
}
